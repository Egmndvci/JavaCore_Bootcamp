package com.egemendeveci._notes_;

public class _06_math {
    public static void main(String[] args) {
        System.out.println("en küçük "+Math.min(3,10)); // min
        System.out.println(Math.max(3,10)); // max
        System.out.println(Math.sqrt(64)); // karekök
        System.out.println(Math.abs(-4.7)); // mutlak değer
        System.out.println(Math.random()); // rastgele sayı
        System.out.println(Math.pow(2,5)); // üs alma
        System.out.println(Math.floor(2.5)); // aşağı yuvarlama
        System.out.println(Math.ceil(2.5)); // yukarı yuvarlama
        System.out.println(Math.round(2.5)); // yuvarlama
        System.out.println(Math.sin(90)); // sinüs





    }
}
